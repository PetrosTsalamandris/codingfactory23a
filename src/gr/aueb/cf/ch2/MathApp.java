package gr.aueb.cf.ch2;

/**
 * Demonstrates The Math class
 */
public class MathApp {
    public static void main(String[] args) {
        System.out.println("Το απολυτο του -5 ειναι:" + Math.abs(-5));
        System.out.println("Το min μεταξυ 1, 10 ειναι: " + Math.min(1, 10));
        System.out.println("Το Max μεταξυ 1, 10 ειναι: " + Math.max(1, 10));
    }
}
